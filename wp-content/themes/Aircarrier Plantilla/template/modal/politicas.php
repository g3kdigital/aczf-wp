    <h6 class="titulo item numero">
      SOLICITUD DE AUTORIZACIÓN AL TITULAR DEL DATO PERSONAL: 
    </h6>
    <p class="parrafo">
      Con antelación y/o al momento de efectuar la recolección del dato personal, <i class="nombre-empresa"><b>AIR</b>CARRIER LOGISTICS SAS</i> solicitará al titular del dato su autorización para efectuar su recolección y tratamiento, indicando la finalidad para la cual se solicita el dato, utilizando para esos efectos medios técnicos automatizados, escritos u orales, que permitan conservar prueba de la autorización y/o de la conducta inequívoca descrita en el artículo 7 del Decreto 1377 de 2013.
    </p>
    <p class="parrafo">
      Dicha autorización se solicitará por el tiempo que sea razonable y necesario para satisfacer las necesidades que dieron origen a la solicitud del dato y, en todo caso, con observancia de las disposiciones legales que rigen sobre la materia. 
    </p>
    <h6 class="titulo item numero">
      LIMITACIONES TEMPORALES AL TRATAMIENTO DE LOS DATOS PERSONALES. 
    </h6>
    <p class="parrafo">
      <i class="nombre-empresa"><b>AIR</b>CARRIER LOGISTICS SAS</i> solo podrá recolectar, almacenar, usar o circular los datos personales durante el tiempo que sea razonable y necesario, de acuerdo con las finalidades que justificaron el tratamiento, atendiendo a las disposiciones aplicables a la materia de que se trate y a los aspectos administrativos, contables, fiscales, jurídicos e históricos de la información. Una vez cumplida la o las finalidades del tratamiento y sin perjuicio de normas legales que dispongan lo contrario, procederá a la supresión de los datos personales en su posesión. No obstante lo anterior, los datos personales deberán ser conservados cuando así se requiera para el cumplimiento de una obligación legal o contractual. 
    </p> 
    <h6 class="titulo item numero">
      ÁREA RESPONSABLE Y PROCEDIMIENTO PARA EL EJERCICIO DE LOS DERECHOS DE LOS TITULARES DEL DATO PERSONAL:
    </h6>
    <p class="parrafo">
      El área administrativa y financiera de <i class="nombre-empresa"><b>AIR</b>CARRIER LOGISTICS SAS</i> será la responsable de atender las peticiones, quejas y reclamos que formule el titular del dato en ejercicio de los derechos contemplados en el numeral 5 de la presente política. La petición, queja o reclamo deberá contener la identificación del Titular, la descripción de los hechos que dan lugar al reclamo, la dirección, y acompañando los documentos que se quiera hacer valer. Si el reclamo resulta incompleto, se requerirá al interesado dentro de los cinco (5) días siguientes a la recepción del reclamo para que subsane las fallas. Transcurridos dos (2) meses desde la fecha del requerimiento, sin que el solicitante presente la información requerida, se entenderá que ha desistido del reclamo. En caso de que quien reciba el reclamo no sea competente para resolverlo, dará traslado a quien corresponda en un término máximo de dos (2) días hábiles e informará de la situación al interesado. El término máximo para atender el reclamo será de quince (15) días hábiles contados a partir del día siguiente a la fecha de su recibo. 
    </p> 
    <p class="parrafo">
      Cuando no fuere posible atender el reclamo dentro de dicho término, se informará al interesado los motivos de la demora y la fecha en que se atenderá su reclamo, la cual en ningún caso podrá superar los ocho (8) días hábiles siguientes al vencimiento del primer término. 
    </p>
