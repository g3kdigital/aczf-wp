    </p>
    <ol class="listado parrafo letra before-color14">
      <li> 
        <b>Autorización:</b> Consentimiento previo, expreso e informado del Titular para llevar a cabo el Tratamiento de datos personales.
      </li>
      <li>
        <b>Aviso de privacidad:</b> Documento físico, electrónico o en cualquier otro formato generado por el Responsable que se pone a disposición del Titular para el tratamiento de sus datos personales. En el Aviso de Privacidad se comunica al Titular la información relativa a la existencia de las políticas de tratamiento de información que le serán aplicables, la forma de acceder a las mismas y la finalidad del tratamiento que se pretende dar a los datos personales.
      </li>
      <li>
        <b>Base de Datos:</b> Conjunto organizado de datos personales que sea objeto de Tratamiento.
      </li>
      <li>
        <b>Dato personal:</b> Cualquier información vinculada o que pueda asociarse a una o varias personas naturales determinadas o determinables.
      </li>
      <li>
        <b>Dato privado:</b> Es el dato que por su naturaleza íntima o reservada sólo es relevante para el titular;
      </li>
      <li>
          <b>Dato público:</b> Es el dato calificado como tal según los mandatos de la ley o de la Constitución Política y aquel que no sea semiprivado, privado o sensible. Son públicos, entre otros, los datos relativos al estado civil de las personas, a su profesión u oficio, a su calidad de comerciante o de servidor público y aquellos que puedan obtenerse sin reserva alguna. Por su naturaleza, los datos públicos pueden estar contenidos, entre otros, en registros públicos, documentos públicos, gacetas y boletines oficiales.
      </li>
      <li>
          <b>Datos sensibles:</b> Se entiende por datos sensibles aquellos que afectan la intimidad del Titular o cuyo uso indebido puede generar su discriminación, tales como aquellos que revelen el origen racial o étnico, la orientación política, las convicciones religiosas o filosóficas, la pertenencia a sindicatos, organizaciones sociales, de derechos humanos o que promueva intereses de cualquier partido político o que garanticen los derechos y garantías de partidos políticos de oposición, así como los datos relativos a la salud, a la vida sexual y los datos biométricos.
      </li>
      <li>
          <b>Encargado del Tratamiento:</b> Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, realice el Tratamiento de datos personales por cuenta del Responsable del Tratamiento.
      </li>
      <li>
          <b>Habeas Data:</b> Derecho de cualquier persona a conocer, actualizar y rectificar las informaciones que se hayan recogido sobre ellas en el banco de datos y/o tratamiento de los datos
      </li>
      <li>
          <b>Responsable del Tratamiento:</b> Persona natural o jurídica, pública o privada, que por sí misma o en asociocon otros, decida sobre la base de datos y/o el Tratamiento de los datos.
      </li>
      <li>
          <b>Titular:</b> Persona natural cuyos datos personales sean objeto de Tratamiento;
      </li>
      <li>
          <b>Tratamiento:</b> Cualquier operación o conjunto de operaciones sobre datos personales, tales como la recolección, almacenamiento, uso, circulación o supresión de los mismos.
      </li>
    </ol> 
    <h6 class="titulo item numero">
      PRINCIPIOS APLICABLES AL TRATAMIENTO DE DATOS PERSONALES:
    </h6>
    <p class="parrafo">
      El tratamiento de datos personales en <i class="nombre-empresa"><b>AIR</b>CARRIER LOGISTICS SAS</i> se regirá por los siguientes principios:
    </p>
    <ol class="listado parrafo letra before-color14">
      <li> 
        <b>Principio de Legalidad:</b> El Tratamiento de datos es una actividad que debe sujetarse a lo establecido en la Ley y las demás disposiciones reglamentarias.Principio de finalidad:</b> El Tratamiento de los datos personales recogidos debe una finalidad legítima, la cual debe ser informada al Titular
      </li>
      <li>
        <b>Principio de finalidad:</b> El Tratamiento de los datos personales recogidos debe obedecer a una finalidad legítima, la cual debe ser informada al Titular          
      </li>
      <li>
        <b>Principio de libertad:</b> El Tratamiento sólo puede llevarse a cabo con el consentimiento, previo, expreso e informado del Titular. Los datos personales no podrán ser obtenidos o divulgados sin previa autorización, o en ausencia de mandato legal o judicial que releve el consentimiento.          
      </li>
      <li>
        <b>Principio de veracidad o calidad:</b> La información sujeta a Tratamiento debe ser veraz, completa, exacta, actualizada, comprobable y comprensible. No será efectuado el Tratamiento de datos parciales, incompletos, fraccionados o que induzcan a error.
       </li>
      <li>
        <b>Principio de transparencia:</b> En el tratamiento debe garantizarse el derecho del titular a obtener de
      <i class="nombre-empresa"><b>AIR</b>CARRIER LOGISTICS SAS</i> en cualquier momento y sin restricciones, información acerca de la existencia de datos que le conciernan.
       </li>
      <li>
        <b>Principio de acceso y circulación restringida:</b> El Tratamiento se sujeta a los límites que se derivan de la naturaleza de los datos personales, de las disposiciones de la ley y la Constitución. En este sentido, el tratamiento solo podrá hacerse por personas autorizadas por el titular y/o por las personas previstas en la ley. Los datos personales, salvo la información pública y lo dispuesto en la autorización otorgada por el titular del dato, no podrán estar disponibles en Internet u otros medios de divulgación o comunicación masiva, salvo que el acceso sea técnicamente controlable para brindar un conocimiento restringido sólo a los Titulares o terceros autorizados
       </li>
      <li>
        <b>Principio de seguridad:</b> La información sujeta a Tratamiento por parte de <i class="nombre-empresa"><b>AIR</b>CARRIER LOGISTICS SAS</i> se deberá proteger mediante el uso de las medidas técnicas, humanas y administrativas que sean necesarias para otorgar seguridad a los registros evitando su adulteración, pérdida, consulta, uso o acceso no autorizado o fraudulento.
       </li>
      <li>
        <b>Principio de confidencialidad:</b> Todas las personas que intervengan en el Tratamiento de datos personales están obligadas a garantizar la reserva de la información, inclusive después de finalizada su relación con alguna de las labores que comprende el Tratamiento. PARÁGRAFO PRIMERO: En el evento que se recolecten datos personales sensibles, el Titular podrá negarse a autorizar su Tratamiento. 
      </li>
    </ol> 
    <h6 class="titulo item numero">
      DERECHOS DE LOS TITULARES DE DATOS PERSONALES OBJETO DE TRATAMIENTO POR PARTE DE AIRCARRIER LOGISTICS SAS:
    </h6>
    <p class="parrafo">
      Los titulares de datos personales por sí o por intermedio de su representante y/o apoderado podrán ejercer los siguientes derechos, respecto de los datos personales que sean objeto de tratamiento por parte de <i class="nombre-empresa"><b>AIR</b>CARRIER LOGISTICS SAS</i> 
    </p>
    <ol class="listado parrafo letra">
      <li>
        Conocer, actualizar y rectificar sus datos personales ante <i class="nombre-empresa"><b>AIR</b>CARRIER LOGISTICS SAS</i>, en su condición de responsable del tratamiento. Este derecho se podrá ejercer, entre otros frente a datos parciales, inexactos, incompletos, fraccionados, que induzcan a error, o aquellos cuyo tratamiento esté expresamente prohibido o no haya sido autorizado.
      </li>
      <li>
        Solicitar prueba de la autorización otorgada a <i class="nombre-empresa"><b>AIR</b>CARRIER LOGISTICS SAS</i> salvo cuando expresamente se exceptúe como requisito para el tratamiento (casos en los cuales no es necesaria la autorización)
      </li>
      <li>
        Ser informado por <i class="nombre-empresa"><b>AIR</b>CARRIER LOGISTICS SAS</i>, previa solicitud respecto del uso que le ha dado a sus datos personales.
      </li>
      <li>
        Presentar ante la Superintendencia de Industria y Comercio quejas por infracciones a lo dispuesto en la ley 1581 de 2012 y las demás normas que la modifiquen, adiciones o complementen.
      </li>
      <li>
        Revocar la autorización y/o solicitar la supresión del dato cuando en el tratamiento no se respeten los principios, derechos y garantías constitucionales y legales.
      </li>
      <li>
        Acceder en forma gratuita a sus datos personales que hayan sido objeto de tratamiento.
      </li>
    </ol>
